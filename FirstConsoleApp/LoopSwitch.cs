using System;

namespace FirstConsoleApp
{
    internal class LoopSwitch
    {
        static void Main(string[] args)
        {
            //Program to display sum of first n natural numbers
            int n, sum;
            Console.Write("Enter the number: ");
            n = int.Parse(Console.ReadLine());
            sum = 0;
            for (int i = 1; i <= n; i++)
            {
                sum += i;
            }
            Console.WriteLine("Sum of first {0} natural numbers is : {1}", n, sum);
            Console.ReadLine();


            //Program to use switch case for calculator.
            int n,a,b;
            Console.WriteLine("*********Calculator*********");
            Console.WriteLine("CODES:\n1. Addition\n2.Subtraction\n3. Multiplication\n4. Division");
            Console.WriteLine("Enter 2 numbers to be used for the Operation:");
            a = int.Parse(Console.ReadLine());
            b = int.Parse(Console.ReadLine());
            Console.Write("Enter the code for operation: ");
            n = int.Parse(Console.ReadLine());
            switch (n)
            {
                case 1: Console.WriteLine("Addition : {0} + {1} = {2}",a,b,a+b);
                     break;
                case 2: Console.WriteLine("Subtraction: {0} - {1} = {2}", a, b, a - b);
                    break;
                case 3: Console.WriteLine("Multiplication: {0} x {1} = {2}", a, b, a * b);
                    break;
                case 4: Console.WriteLine("Subtraction: {0} / {1} = {2}", a, b, a / b);
                    break;
                default: Console.WriteLine("Invalid Code.");
                    break;
            }
            Console.ReadLine();
        }

    }
}
